def test_report_includes_human_review_disclaimer():
    request = ReportRequest(msa_filename="msa.pdf", sow_filename="sow.pdf")
    text = _extract_text(generate_report_pdf(request))
    assert "human review" in text


def test_missing_deadline_and_reviewer_render_without_mojibake():
    """Regression guard: an em-dash ('—') placeholder for a missing
    deadline/reviewer/section rendered as a replacement-character glyph
    ('�') when extracted from the PDF, in both reportlab's Paragraph
    markup layer and canvas.drawString -- found by inspecting a real
    generated report. Fixed by using an ASCII '--' fallback instead,
    matching the convention already used throughout this codebase's source
    comments for the same reason."""
    obligation = _obligation(section_number=None, deadline_days=None)
    audit_entry = _audit_entry(reviewer=None)
    request = ReportRequest(msa_filename="msa.pdf", sow_filename="sow.pdf", obligations=[obligation], audit_entries=[audit_entry])
    text = _extract_text(generate_report_pdf(request))
    assert "�" not in text
    assert "—" not in text


def test_special_characters_in_clause_text_do_not_break_pdf_generation():
    """reportlab's Paragraph uses HTML-like markup -- unescaped '<', '>', '&'
    in AI-generated explanations or clause text must not crash rendering."""
    flag = _risk_flag(title="A & B <conflict>", description="Uses < and > and & characters")
    request = ReportRequest(msa_filename="msa.pdf", sow_filename="sow.pdf", risk_flags=[flag])
    text = _extract_text(generate_report_pdf(request))
    assert "A & B <conflict>" in text
